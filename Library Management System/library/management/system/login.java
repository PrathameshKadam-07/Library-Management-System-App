    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            Connection c = Connect.Connection();
            PreparedStatement pst = c.prepareStatement("select * from login where userid=? and password=?"); 
            
            pst.setString(1, txtuserid.getText());
            pst.setString(2, txtpassword.getText());
            
            ResultSet rs = pst.executeQuery(); 
            if(rs.next()){
                new Home().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(this,"Please Enter Valid Id Password");
            }
            
        } catch (SQLException ex) {
            System.getLogger(login.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

